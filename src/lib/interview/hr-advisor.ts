// =============================================================================
// AI HR Interview Advisor вҖ” deterministic, industry-aware prep generator.
// -----------------------------------------------------------------------------
// Runs AFTER a CV is successfully synthesized. It inspects the finished
// `MakaraCvDraft` (target role, years, top skills, latest employer) and returns
// a tailored "Interview Prep Guide": the questions a recruiter most frequently
// asks for THAT exact position, each paired with a strong, ready-to-adapt model
// answer вҖ” all in Khmer.
//
// Always returns >= 5 items (3 universal + >= 4 industry-specific, deduped).
// Rule-based + fully deterministic (no key required); the API layer may add LLM
// polish on top, but the plan + personalization live here so the feature never
// depends on an external service.
// =============================================================================

/** Personalization signals distilled from the finished draft. */
interface Signals {
  role: string;
  years: string;
  skillsLabel: string;
  company: string;
  industryLabel: string;
}

function deriveSignals(industryId: IndustryId, draft: MakaraCvDraft): Signals {
  const industry = getIndustry(industryId) ?? getIndustry("other")!;
  const role = draft.headline.trim() || draft.experience[0]?.role?.trim() || "бһҸбһҪбһ“бһ¶бһ‘бһёбһ“бҹҒбҹҮ";
  const yearsMatch = draft.summary.match(/(\d+)\s*бһҶбҹ’бһ“бһ¶бҹҶ/);
  const years = yearsMatch ? yearsMatch[1] : "";
  const topSkills = draft.skills.map((s) => s.trim()).filter(Boolean).slice(0, 3);
  const skillsLabel = topSkills.length > 0 ? topSkills.join(", ") : "бһҮбҹҶбһ“бһ¶бһүбһҹбҹ’бһ“бһјбһӣбһҡбһ”бһҹбҹӢбһҒбҹ’бһүбһ»бҹҶ";
  const company = draft.experience[0]?.company?.trim() ?? "";
  return { role, years, skillsLabel, company, industryLabel: industry.label };
}

