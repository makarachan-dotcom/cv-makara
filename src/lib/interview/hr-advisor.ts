/**
 * Build the tailored Interview Prep Guide for a finished CV draft.
 * Guarantees at least 5 unique question/answer pairs.
 */
export function buildInterviewPrep(
  industryId: IndustryId,
  draft: MakaraCvDraft,
): InterviewPrepItem[] {
  const signals = deriveSignals(industryId, draft);
  const industryFn = INDUSTRY_BANK[industryId] ?? INDUSTRY_BANK.other;

  const merged = [...universalQuestions(signals), ...industryFn(signals)];

  const seen = new Set<string>();
  const unique: InterviewPrepItem[] = [];
  for (const item of merged) {
    const key = item.question.trim();
    if (seen.has(key)) continue;
    seen.add(key);
    unique.push({ question: item.question.trim(), answer: item.answer.trim() });
  }

  if (unique.length < 5) {
    for (const item of INDUSTRY_BANK.other(signals)) {
      const key = item.question.trim();
      if (seen.has(key)) continue;
      seen.add(key);
      unique.push({ question: item.question.trim(), answer: item.answer.trim() });
      if (unique.length >= 5) break;
    }
  }

  return unique.slice(0, 12);
}
